"""LLM-as-judge metrics for RAG evaluation."""

from __future__ import annotations

import re
from typing import Callable

FAITHFULNESS_PROMPT = """You are an evaluator. Given a QUESTION, an ANSWER generated by a system, and the CONTEXT that was given to the system, determine whether the answer is fully supported by the context (faithful). Do not penalize minor wording differences.

QUESTION: {question}

CONTEXT: {context}

ANSWER: {answer}

Respond with only a single number from 0 to 10:
- 0-3: The answer contains claims not supported by the context or contradicts the context.
- 4-6: The answer is partially supported; some claims are in the context, others are not.
- 7-10: The answer is fully supported by the context (faithful).

Score:"""

ANSWER_RELEVANCY_PROMPT = """You are an evaluator. Given a QUESTION and an ANSWER, determine how relevant the answer is to the question (does it address what was asked?).

QUESTION: {question}

ANSWER: {answer}

Respond with only a single number from 0 to 10:
- 0-3: The answer is irrelevant or does not address the question.
- 4-6: The answer partially addresses the question.
- 7-10: The answer is fully relevant and addresses the question.

Score:"""


def _normalize_score(llm_output: str) -> float | None:
    """Extract a 0-1 score from LLM output. Returns None if parsing fails."""
    # Try to find a number 0-10 or 0.0-1.0
    numbers = re.findall(r"\b(\d+(?:\.\d+)?)\b", llm_output.strip())
    if not numbers:
        return None
    score = float(numbers[0])
    if score > 1:
        score = score / 10.0
    return max(0.0, min(1.0, score))


def compute_faithfulness(
    question: str,
    answer: str,
    context: str,
    llm_fn: Callable[[str], str],
) -> float | None:
    """Compute faithfulness: is the answer supported by the context?

    Args:
        question: User question.
        answer: Generated answer.
        context: Retrieved context (concatenated).
        llm_fn: Callable that takes a prompt string and returns model output.

    Returns:
        Score in [0, 1] or None if parsing failed.
    """
    if not context.strip():
        return None
    prompt = FAITHFULNESS_PROMPT.format(
        question=question,
        context=context[:8000],
        answer=answer or "(no answer)",
    )
    out = llm_fn(prompt)
    return _normalize_score(out)


def compute_answer_relevancy(
    question: str,
    answer: str,
    llm_fn: Callable[[str], str],
) -> float | None:
    """Compute answer relevancy: does the answer address the question?

    Args:
        question: User question.
        answer: Generated answer.
        llm_fn: Callable that takes a prompt string and returns model output.

    Returns:
        Score in [0, 1] or None if parsing failed.
    """
    prompt = ANSWER_RELEVANCY_PROMPT.format(
        question=question,
        answer=answer or "(no answer)",
    )
    out = llm_fn(prompt)
    return _normalize_score(out)
